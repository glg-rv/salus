.option norvc

.section .text.start

// The entry point for a task.
.global _start
_start:

.option push
.option norelax
    la gp, __global_pointer$
.option pop
    la sp, _stack_end

    la t1, task_main
    la ra, 1f
    jr t1

    j 1f
1:
    // ecall to exit
    ecall

